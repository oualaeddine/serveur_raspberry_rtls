#!/bin/bash

echo "--------------------------------------------------------------------------------"
echo "------------------------------- Update -----------------------------------------"
echo "--------------------------------------------------------------------------------"

sudo apt update

echo "--------------------------------------------------------------------------------"
echo "------------------------------- Upgrade ----------------------------------------"
echo "--------------------------------------------------------------------------------"

sudo apt upgrade

echo "--------------------------------------------------------------------------------"
echo "--------------------- Remove older version of nodejs ---------------------------"
echo "--------------------------------------------------------------------------------"

sudo apt remove nodered -y
sudo apt remove node nodejs nodejs-legacy -y
sudo apt remove npm -y

sudo apt remove --purge node

echo "--------------------------------------------------------------------------------"
echo "----------------------------- Installation of nodejs ---------------------------"
echo "--------------------------------------------------------------------------------"

curl -sL http://deb.nodesource.com/setup_8.x | sudo bash -

sudo apt-get install -y nodejs

echo "--------------------------------------------------------------------------------"
echo "-------------- Installation of librairies necessaries to program  --------------"
echo "--------------------------------------------------------------------------------"

sudo apt install -y libcap2-bin

echo "--------------------------------------------------------------------------------"
echo "---------------------------- Update project package  ---------------------------"
echo "--------------------------------------------------------------------------------"

cd .. ; sudo npm install --save ; cd scripts/

echo "--------------------------------------------------------------------------------"
echo "------------------ Installation of MQTT broker (mosquitto)  --------------------"
echo "--------------------------------------------------------------------------------"

sudo apt -y install mosquitto

echo "--------------------------------------------------------------------------------"
echo "------------- Configuration of the LAN : setup an static ipaddress  ------------"
echo "--------------------------------------------------------------------------------"

sudo nano /etc/dhcpcd.conf

echo "--------------------------------------------------------------------------------"
echo "-------------------------- Configuration of the service  -----------------------"
echo "--------------------------------------------------------------------------------"


sudo setcap cap_net_raw+eip $(eval readlink -f `which node`)
sudo cp serverKtalyse.service /etc/systemd/system/serverKtalyse.service
sudo systemctl enable serverKtalyse.service

echo "--------------------------------------------------------------------------------"
echo "-------------------------- Reboot in five seconds  -----------------------------"
echo "--------------------------------------------------------------------------------"

sudo reboot now
